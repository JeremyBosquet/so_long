#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned int	i;
	int				neg;
	unsigned int	res;

	i = 0;
	neg = 1;
	res = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -neg;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + (str[i] - 48);
		i++;
	}
	if (res > 2147483648 && neg == -1)
		return (0);
	if (res > 2147483648)
		return (-1);
	return (res * neg);
}
